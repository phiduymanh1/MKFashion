package com.example.shopquanaomk.Dto.ResponDto;

import java.math.BigDecimal;

public class ProductDtoResponse {
    private Integer id;
    private String name;
    private String description;
    private BigDecimal price;
    private Integer stock;

    private String colorName;
    private String sizeName;
    private String brandName;
    private String nameCategory;
    private String imageUrl;

    public ProductDtoResponse() {
    }

    public ProductDtoResponse(Integer id, String name, String description, BigDecimal price, Integer stock, String colorName, String sizeName, String brandName, String nameCategory, String imageUrl) {
        this.id = id;
        this.name = name;
        this.description = description;
        this.price = price;
        this.stock = stock;
        this.colorName = colorName;
        this.sizeName = sizeName;
        this.brandName = brandName;
        this.nameCategory = nameCategory;
        this.imageUrl = imageUrl;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public BigDecimal getPrice() {
        return price;
    }

    public void setPrice(BigDecimal price) {
        this.price = price;
    }

    public Integer getStock() {
        return stock;
    }

    public void setStock(Integer stock) {
        this.stock = stock;
    }

    public String getColorName() {
        return colorName;
    }

    public void setColorName(String colorName) {
        this.colorName = colorName;
    }

    public String getSizeName() {
        return sizeName;
    }

    public void setSizeName(String sizeName) {
        this.sizeName = sizeName;
    }

    public String getBrandName() {
        return brandName;
    }

    public void setBrandName(String brandName) {
        this.brandName = brandName;
    }

    public String getNameCategory() {
        return nameCategory;
    }

    public void setNameCategory(String nameCategory) {
        this.nameCategory = nameCategory;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }
}
